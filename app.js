function getRandomNumber() {
    const random = Math.floor(Math.random() * 4)
    return random;
}
function getMood() {
    let mood;
    let result = getRandomNumber();
    if (result === 1 || result === 3) {
        mood = 'Happy'
    } else if (result === 2) {
        mood = 'Sad'
    } else if (result === 0) {
        mood = 'Angry'
    }
    return mood
}
